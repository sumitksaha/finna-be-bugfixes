						<h3>EDUCATIONAL QUALIFICATIONS</h3>
						<p>A degree (Graduation) with 60% (55% for SC/ST/PWD) marks in any discipline from a recognized University or any equivalent qualification recognized as such by the Central Government. </p>
						<p><b>Computer Literacy</b>: Operating and working knowledge in computer systems is mandatory i.e. candidates should have Certificate/Diploma/Degree in computer operations/Language/ should have studied Computer / Information Technology as one of the subjects in the High School/College/Institute.</p>
						<h3>PENALTY FOR WRONG ANSWERS</h3>
						<p>There will be penalty for wrong answers marked in the Objective Test. For each question for which a wrong answer has been given by the candidate one fourth or 0.25 of the marks assigned to that question will be deducted as penalty to arrive at corrected score.</p>

						<h3>CUTOFF SCORE</h3>
						<p>Each candidate will have to secure a minimum standardized score in each of the tests to qualify in the written examination and to be considered for vacancies in the participating banks. The cut-off points will be decided based on the average (Average – ¼ Standard Deviation for General category candidates and Average – ¾ Standard Deviation for Reserved Category candidates). </p>
